#include <bits/stdc++.h>
using namespace std;
int getMaxDiff(int a[],int n)
{
    int i,currDiff;
    stack<int> mins,maxs;
    int min=0;
    mins.push(min);
    int maxDiff=0;
    for(i=1;i<n;i++)
    {
        if(a[min]<a[i])
            maxs.push(i);
        else
        {
            min=i;
            mins.push(i);
        }
    }
    int left,right;
    while(!mins.empty() && !maxs.empty())
    {
        left=mins.top();
        right=maxs.top();
        cout<<left<<" "<<right<<endl;
        if(a[right]>a[left])
        {
            if(maxDiff<right-left)
                maxDiff=right-left;
            mins.pop();
        }
        else
            maxs.pop();
    }
    return maxDiff;
}
int main()
{
	int t,i,n;
	cin>>t;
	while(t--)
	{
	    cin>>n;
	    int a[n];
	    for(i=0;i<n;i++)
	        cin>>a[i];
	    cout<<getMaxDiff(a,n)<<endl;
	}
	return 0;
}
